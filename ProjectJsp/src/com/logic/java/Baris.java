package com.logic.java;

//penamaan kelas pada java dengan nama kelas Baris dg bertipekan public
public class Baris {
	
	//method main yang bertipe static untuk memangil kelas/instance kelas di java, menjalankan kelas java
	public static void main(String[] args) {
		//loop untuk melakukan perulangan incremen(naik) dari 0 sampai kuarang dari 10 
		for (int i = 0; i < 10; i++) {
			//system.out.print untuk mencetak baris di consule
			System.out.print("*");
		}
	}

}
